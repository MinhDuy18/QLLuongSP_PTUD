    public void loadDataToTablePCCN() {
        try {
            PhanCongCNDao1 pccn_dao = new PhanCongCNDao1();
            List<PhanCong1> list = pccn_dao.getDSPhanCong();
            model.setRowCount(0);
            for (PhanCong1 pccn : list) {
                {
                    model.addRow(new Object[]{
                        pccn.getMaPC(),
                        pccn.getCongDoan().getMaPhanDoan(),
                        pccn.getCongNhan().getMaCongNhan(),
                        pccn.getSoLuongCanLam(),
                        pccn.getNgayPhanCong(),
                        pccn.getNgayHoanThanh(),
                        pccn.getTrangThai()
                    });
                }
            }
            model.fireTableDataChanged();
        } catch (Exception e) {
        }
    }
